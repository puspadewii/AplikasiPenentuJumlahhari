    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        if(textTahun.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Error: isian tahun kosong!", "Error: isian tahun kosong!", JOptionPane.WARNING_MESSAGE);  
        } else{
            int tahun = Integer.parseInt(textTahun.getText());
            int jumlahHari;
            if(cboxBulan.getSelectedItem().equals("Februari")){
                if((tahun % 4 == 0) && !(tahun % 100 == 0) || (tahun % 4 == 0))
                    jumlahHari = 29;
                else jumlahHari = 28;
            }else if(cboxBulan.getSelectedItem().equals("April") ||
                    cboxBulan.getSelectedItem().equals("Juni") ||
                    cboxBulan.getSelectedItem().equals("September") ||
                    cboxBulan.getSelectedItem().equals("November")){
                jumlahHari = 30;
            }else{
                jumlahHari = 31;
            }
            jumlahHariLabel.setText("Jumlah hari pada bulan " + cboxBulan.getSelectedItem() + " tahun " + tahun + " adalah " + jumlahHari);
        }
    }//GEN-LAST:event_btnHitungActionPerformed

